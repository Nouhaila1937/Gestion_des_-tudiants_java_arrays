private void ajouterEtudiant() {
    model = (DefaultTableModel) tableetudiant.getModel();
    boolean ajout = true;
    
    // Vérification si les champs sont vides
    if (txtnom.getText().trim().equals("") || 
        txtprenom.getText().trim().equals("") || 
        txtage.getText().trim().equals("")) {
        
        JOptionPane.showMessageDialog(this, "Remplissez tous les champs SVP", "Champs vides", JOptionPane.ERROR_MESSAGE);
        ajout = false;
    }
for(Etudiant e:etudiants){
if(e.getNom().equals(txtnom.getText()) && e.getPrenom().equals(txtprenom.getText()) ){
 JOptionPane.showMessageDialog(this, "Cet éléve est déjà inscrit", "Elève existe déjà", JOptionPane.ERROR_MESSAGE);
         ajout = false;
              
}
}
    // Si les champs ne sont pas vides, on essaye de créer l'étudiant
    if (ajout) {
        try {
            // Conversion de l'âge
            int age = Integer.parseInt(txtage.getText().trim());
            
            // Création de l'étudiant
            Etudiant etudiant = new Etudiant(txtnom.getText().trim(), txtprenom.getText().trim(), age);
            
            // Ajout de l'étudiant à la liste et au tableau
            etudiants.add(etudiant);
            model.addRow(new Object[]{etudiant.getInscription(), etudiant.getNom(), etudiant.getPrenom(), etudiant.getAge()});
              viderChamp();
        } catch (NumberFormatException e) {
            // Si l'âge n'est pas un entier, on affiche un message d'erreur
            JOptionPane.showMessageDialog(this, "L'âge doit être un nombre entier", "Erreur de saisie", JOptionPane.ERROR_MESSAGE);
        }
    }
}

    private void viderChamp() {
        txtnom.setText(" ");
        txtprenom.setText(" ");
        txtage.setText(" ");
    }

    private void modifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierActionPerformed
        // TODO add your handling code here:
           model = (DefaultTableModel) tableetudiant.getModel();
        int ligne=tableetudiant.getSelectedRow();
        if(ligne!=-1){
            model.setValueAt(txtnom.getText(), ligne, 1);
            model.setValueAt(txtprenom.getText(), ligne, 2);
            model.setValueAt(txtage.getText(), ligne, 3);
            etudiants.get(ligne).setNom(txtnom.getText());
            etudiants.get(ligne).setPrenom(txtprenom.getText());
            etudiants.get(ligne).setAge(Integer.valueOf( txtage.getText()));
        }
    }//GEN-LAST:event_modifierActionPerformed

