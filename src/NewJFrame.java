    private void chercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chercherActionPerformed
        // TODO add your handling code here:
         model = (DefaultTableModel) tableetudiant.getModel();
         boolean trouve=false;
         int indice=0;
       if(txtchercher.getText().equals(""))
              JOptionPane.showMessageDialog(this, "Entrer un NOM SVP", "Erreur de saisie", JOptionPane.ERROR_MESSAGE);
       else {
             String nomchercher=txtchercher.getText();
             for(int i=0;i<etudiants.size();i++){ // pour avoir l'indice ou se trouve le nom chercher
                 Etudiant e=etudiants.get(i); // création d'un objet 
                 if(nomchercher.equals(e.getNom())){
                 trouve=true;
                 indice=i;
                 break;
                 }
             }
        }

        if(trouve) //si on a trouuvé l'étudiant chercher alors on va 
        {
            txtnom.setText(etudiants.get(indice).getNom());
            txtprenom.setText(etudiants.get(indice).getPrenom());
            txtage.setText(String.valueOf(etudiants.get(indice).getAge()));
        }
        else{
              JOptionPane.showMessageDialog(this, "L'élève n'existe pas dans la liste", "Erreur de saisie", JOptionPane.ERROR_MESSAGE);
     
        }
    }//GEN-LAST:event_chercherActionPerformed

